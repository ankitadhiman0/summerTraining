# swapping using three variable
a = 50
b = 78
print("before swapping")
print("a = ",a,"b = ",b)
c = a
a = b
b = c
print("after swapping")
print("a = ",a,"b = ",b)

# swapping using two variable
x = 78
y = 68
print("before swapping")
print("x = ",x,"y = ",y)
a ,b = b,a
print("after swapping")
print("x = ",x,"y = ",y)